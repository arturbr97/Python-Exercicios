# Faça um Programa que verifique se uma letra digitada é vogal ou consoante.

letra = str(input('Digite uma Letra: ').upper())

if letra == 'A':
    print('Vogal')
elif letra == 'E':
    print('Vogal')
elif letra == 'I':
    print('Vogal')
elif letra == 'O':
    print('Vogal')
elif letra == 'U':
    print('Vogal')

else :
    print('Consoante')
    
